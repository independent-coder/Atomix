export function getTranslation(lang, key) {
    const keys = key.split('.');
    let value = translations[lang];
    
    for (const k of keys) {
        value = value?.[k];
    }
    
    return value || translations.en[key] || key;
}

export function getElementName(lang, symbol) {
    return translations[lang]?.elements?.[symbol] || translations.en.elements[symbol] || symbol;
}
